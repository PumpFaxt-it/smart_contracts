async function getCurrentHolders(
  provider: ethers.providers.Provider,
  tokenAddress: string
): Promise<{ address: string; balance: ethers.BigNumber }[]> {
  // Object to store balances
  const balances: { [address: string]: ethers.BigNumber } = {};

  // Create a contract instance
  const tokenContract = new ethers.Contract(tokenAddress, ERC20_ABI, provider);

  // Fetch all transfer events
  const filter = {
    address: tokenAddress,
    fromBlock: 0,
    toBlock: "latest",
    topics: [ethers.utils.id("Transfer(address,address,uint256)")],
  };

  const logs = await provider.getLogs(filter);

  // Process each event to update balances
  logs.forEach((log) => {
    const parsedLog = tokenContract.interface.parseLog(log);
    const { from, to, value } = parsedLog.args;

    if (!balances[from]) {
      balances[from] = ethers.BigNumber.from(0);
    }
    if (!balances[to]) {
      balances[to] = ethers.BigNumber.from(0);
    }

    balances[from] = balances[from].sub(value);
    balances[to] = balances[to].add(value);
  });

  // Filter out addresses with zero balance
  const holders = Object.keys(balances).filter(
    (address) => !balances[address].isZero()
  );

  return holders.map((address) => ({ address, balance: balances[address] }));
}
